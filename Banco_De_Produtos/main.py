class Produto:
    def __init__(self, id_produto, nome, categoria, qtd_estoque, preco, desc, fornecedor):
        self.id_produto = id_produto
        self.nome = nome
        self.categoria = categoria
        self.qtd_estoque = qtd_estoque
        self.preco = preco
        self.desc = desc
        self.fornecedor = fornecedor